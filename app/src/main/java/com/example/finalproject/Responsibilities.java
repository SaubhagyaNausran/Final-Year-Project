package com.example.finalproject;

import java.util.List;

public class Responsibilities {
    private List<String> description;
    private List<String> positions_of_responsibility;

    // Getters and Setters
    public List<String> getDescription() {
        return description;
    }

    public void setDescription(List<String> description) {
        this.description = description;
    }

    public List<String> getPositionsOfResponsibility() {
        return positions_of_responsibility;
    }

    public void setPositionsOfResponsibility(List<String> positions_of_responsibility) {
        this.positions_of_responsibility = positions_of_responsibility;
    }
}
